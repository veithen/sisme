package com.googlecode.sisme.description;

import com.googlecode.sisme.Type;

public class TypeMappingKey {
    private final Type from;
    private final Type to;
    
    public TypeMappingKey(Type from, Type to) {
        this.from = from;
        this.to = to;
    }

    // TODO: clean this up
    @Override
    public int hashCode() {
        final int prime = 31;
        int result = 1;
        result = prime * result + ((from == null) ? 0 : from.hashCode());
        result = prime * result + ((to == null) ? 0 : to.hashCode());
        return result;
    }

    // TODO: clean this up
    @Override
    public boolean equals(Object obj) {
        if (this == obj)
            return true;
        if (obj == null)
            return false;
        if (getClass() != obj.getClass())
            return false;
        TypeMappingKey other = (TypeMappingKey) obj;
        if (from == null) {
            if (other.from != null)
                return false;
        } else if (!from.equals(other.from))
            return false;
        if (to == null) {
            if (other.to != null)
                return false;
        } else if (!to.equals(other.to))
            return false;
        return true;
    }
}
